print("Towards which direction should I paint(up,down,left,right)?")
direction = input()
if (direction == "up"):
  print("I am painting in an upwards direction.")
elif (direction == "down"):
  print("I am painting in a downwards direction.")
elif (direction == "right"):
  print("I am painting in a right direction.")
elif (direction == "left"):
  print("I am painting in a left direction.")